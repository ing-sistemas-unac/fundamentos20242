# Declaración de variables enteras
a = 3
b = 7
# Operaciones con las variables enteras
suma = a + b
print("La suma de", a, "+", b, "es igual a", suma)
resta = a - b
print("La resta de", a, "-", b, "es igual a", resta)
multiplicacion = a * b
print("La multiplicación de", a, "*", b, "es igual a", multiplicacion)
exponenciacion = a ** b # Los ** indican que se eleva
print(a, "elevado a la", b, "es igual a", exponenciacion)
division = a / b
print(a, "dividido entre", b, "es igual a", division)
division_piso = a // b # Elimina los decimales
print(a, "dividido piso // entre", b, "es igual a", division_piso)
modulo = a % b # residuo
print(a, "módulo %", b, "es igual a", modulo)
print(type(division))
