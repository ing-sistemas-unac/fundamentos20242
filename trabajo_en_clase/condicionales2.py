print("Ingrese una vocal")
letra = input().lower()
# Condicional
if letra == "a":
    print("Es la vocal a")
elif letra == "e":
    print("Es la vocal e")
elif letra == "i":
    print("Es la vocal i")
elif letra == "o":
    print("Es la vocal o")
elif letra == "u":
    print("Es la vocal u")
else:
    print("La letra no es la vocal")  